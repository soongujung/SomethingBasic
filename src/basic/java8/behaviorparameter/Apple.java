package basic.java8.behaviorparameter;

public class Apple {
    private int weight = 0;
    private String color = "Red";

    public Apple(){

    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
